# 文字列（Strings）

print("Hello world!!")
print('Hello world!!')
print('1')
print(1)

print("I'm fine")
print('I"m fine')

# クオーテーションマークを3つ使うと改行して書ける
print("""
Hello world!!
How are you doing?
""")

# \nは改行
print('hello \nworld')
# \tはタブ
print('hello \tworld')

# \をつけると後ろの文字を文字列として認識する
print('back slash n:\\n')
print('I\'m fine')

# 文字列は+を使って連結できる
print('hello' + 'world' + '!!')
